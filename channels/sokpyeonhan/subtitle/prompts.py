"""
속편한 밥상 채널 자막 효과 프롬프트
모드별 전용 프롬프트 (공통 규칙 + 모드별 어절 분리 규칙)
"""

# ============================================================
# 공통 규칙 (모든 모드에서 공유)
# ============================================================
_SUBTITLE_COMMON_INTRO = """너는 유튜브 쇼츠 요리 영상의 **자막 효과 연출 전문가**다.
아래 대본을 분석하여, 각 문장의 어절을 자연스러운 단위로 나누고
어떤 어절에 효과를 줄지 결정해라.
또한 강조할 키워드에 색상을 지정해라.

[대본]
{script_text}
"""

_SUBTITLE_COMMON_EFFECTS = """
==============================================
[사용 가능한 효과 — 2가지만 사용]
==============================================
1. "bounce" — 톡 튀어나오는 크기 변화 (강조할 어절에만)
2. null — 효과 없음 (일반 어절)

==============================================
[사용 가능한 글자 색상 — 3가지]
==============================================
1. "#FFD700" — 금색 (요리명, 강조, 감탄)
2. "#FF3333" — 빨간색 (경고, 금지, 부정)
3. "#00FF88" — 녹색 (비법, 팁, 핵심 해결책)
※ 지정 안 된 어절은 자동으로 흰색
"""

_SUBTITLE_COMMON_RULES = """
==============================================
[공통 규칙 — 반드시 지켜라]
==============================================
1. **대본의 모든 scene을** 출력해라
2. **한 문장에 bounce는 최대 2개**까지만 (나머지는 전부 null)
3. **words의 text를 공백으로 이어붙이면 원래 문장과 정확히 같아야 한다**
   - 원문: "고사리 볶을 때 뚜껑 열고 뒤적거리기만 하시나요?"
   - words: ["고사리", "볶을 때", "뚜껑 열고", "뒤적거리기만", "하시나요?"]
   → 합치면: "고사리 볶을 때 뚜껑 열고 뒤적거리기만 하시나요?" ✅
   - **단어를 수정, 생략, 추가하지 마라**
4. **어절 분리는 철저히 '의미 단위(의미상 한 호흡)'로 묶어라 (단순 글자 수 끊기 절대 금지)**
   - (핵심) 명사와 조사, 복합 명사를 절대 중간에 쪼개지 마라.
   - 나쁜 예: ["볶을", "때"] → 너무 짧음 ❌
   - 좋은 예: ["볶을 때"] → 자연스러운 호흡 ✅
5. **color_keywords에는 색상을 줄 키워드만** 포함 (대부분 어절은 지정 불필요)
"""

_SUBTITLE_COMMON_SCENE_GUIDE = """
==============================================
[Scene별 가이드라인]
==============================================
- Scene 1 (Hook 질문): display=single
  → 요리명에 bounce + 금색

- Scene 2 (단점 경고): display=single
  → 부정적/경고 핵심 단어에 bounce + 빨간색

- Scene 3 (원인 설명): display=single
  → 효과 최소 (0~1개, 차분한 구간)

- Scene 4 (전환): display=single
  → 첫 어절에 bounce + 금색

- Scene 5 (질문): display=single
  → "비법이"에 bounce + 금색

- Scene 6 (좋아요 CTA): display=static
  → 문장 전체 1개 어절, bounce + 금색

- Scene 7 (Kick 공개): display=single
  → 핵심 비법 어절에 bounce + 녹색
"""

_SUBTITLE_COMMON_OUTPUT = """
==============================================
[출력 형식]
==============================================
{{
    "scenes": [
        {{
            "scene_id": 1,
            "display": "single",
            "words": [
                {{"text": "고사리", "effect": "bounce"}},
                {{"text": "볶을 때", "effect": null}}
            ]
        }},
        ...
    ],
    "color_keywords": {{
        "#FFD700": ["고사리"],
        "#FF3333": [],
        "#00FF88": []
    }}
}}

⚠️ 위 형식 그대로 JSON만 출력해라. 설명, 부연, 마크다운, 코드블록 금지.
⚠️ 반드시 모든 scene을 포함하고 color_keywords도 포함해라.
"""

# Phrase 모드 전용 Scene 가이드 (display=phrase)
_SUBTITLE_PHRASE_SCENE_GUIDE = """
==============================================
[Scene별 가이드라인 — phrase 모드]
==============================================
- Scene 1 (Hook 질문): display=phrase
  → 요리명에 bounce + 금색
  → 문장을 2~3개 의미 구절로 나눠라

- Scene 2 (단점 경고): display=phrase
  → 부정적/경고 핵심 단어에 bounce + 빨간색
  → 문장을 2~3개 의미 구절로 나눠라

- Scene 3 (원인 설명): display=phrase
  → 효과 최소 (0~1개, 차분한 구간)
  → 문장을 2~3개 의미 구절로 나눠라

- Scene 4 (전환): display=phrase
  → 첫 구절에 bounce + 금색
  → 문장을 2개 의미 구절로 나눠라

- Scene 5 (질문): display=phrase
  → 문장이 짧으면 1개 구절로 통째 출력 가능
  → "비법이"에 bounce + 금색

- Scene 6 (좋아요 CTA): display=static
  → 문장 전체 1개 어절, bounce + 금색

- Scene 7 (Kick 공개): display=phrase
  → 핵심 비법 어절에 bounce + 녹색
  → 문장을 2~3개 의미 구절로 나눠라
"""

# Phrase 모드 전용 출력 형식
_SUBTITLE_PHRASE_OUTPUT = """
==============================================
[출력 형식 — phrase 모드]
==============================================
입력 대본:
1. 고사리 볶을 때 뚜껑 열고 뒤적거리기만 하시나요?

출력 예시:
{{
    "scenes": [
        {{
            "scene_id": 1,
            "display": "phrase",
            "words": [
                {{"text": "고사리 볶을 때", "effect": "bounce"}},
                {{"text": "뚜껑 열고 뒤적거리기만", "effect": null}},
                {{"text": "하시나요?", "effect": null}}
            ]
        }},
        ...
    ],
    "color_keywords": {{
        "#FFD700": ["고사리 볶을 때"],
        "#FF3333": [],
        "#00FF88": []
    }}
}}

⚠️ 위 형식 그대로 JSON만 출력해라. 설명, 부연, 마크다운, 코드블록 금지.
⚠️ 반드시 모든 scene을 포함하고 color_keywords도 포함해라.
⚠️ 각 구절은 반드시 5자 이상이어야 한다. 짧은 구절은 인접 구절과 합쳐라.
"""


# ============================================================
# Single 모드 전용 프롬프트
# ============================================================
SUBTITLE_EFFECT_PROMPT_SINGLE = (
    _SUBTITLE_COMMON_INTRO
    + _SUBTITLE_COMMON_EFFECTS
    + _SUBTITLE_COMMON_RULES
    + """
==============================================
[single 모드 어절 분리 규칙]
==============================================
- 한 어절이 화면 중앙에 **단독으로** 표시됩니다.
- 한 묶음은 최소 **2자**, 최대 **{max_chunk_chars}자**(공백 포함).
- {max_chunk_chars}자를 초과하면 의미 단위 경계에서 분리해라.
"""
    + _SUBTITLE_COMMON_SCENE_GUIDE
    + _SUBTITLE_COMMON_OUTPUT
)


# ============================================================
# Stack 모드 전용 프롬프트
# ============================================================
SUBTITLE_EFFECT_PROMPT_STACK = (
    _SUBTITLE_COMMON_INTRO
    + _SUBTITLE_COMMON_EFFECTS
    + _SUBTITLE_COMMON_RULES
    + """
==============================================
[stack 모드 어절 분리 규칙]
==============================================
- 어절이 2줄로 쌓이며 표시됩니다 (1줄씩 추가, 최대 2줄).
- 한 묶음은 최소 **4자**, 최대 **{max_chunk_chars}자**(공백 포함).
- **3자 이하 어절은 반드시 인접 어절과 합쳐라.**
- 인접한 두 어절의 길이 차이가 크지 않도록 **균등하게** 분리해라.
"""
    + _SUBTITLE_COMMON_SCENE_GUIDE
    + _SUBTITLE_COMMON_OUTPUT
)


# ============================================================
# Phrase 모드 전용 프롬프트
# ============================================================
SUBTITLE_EFFECT_PROMPT_PHRASE = (
    _SUBTITLE_COMMON_INTRO
    + _SUBTITLE_COMMON_EFFECTS
    + _SUBTITLE_COMMON_RULES
    + """
==============================================
[phrase 모드 어절 분리 규칙]
==============================================
- 구절(phrase)이 화면 중앙에 표시되고, 새 구절이 나오면 **이전 구절을 완전히 교체**합니다.
- 한 구절 = **의미상 완결된 한 호흡** (주어+서술어, 명사+조사+동사 등)
- 한 구절은 최소 **5자**, 최대 **{max_chunk_chars}자**(공백 포함).
- {max_chunk_chars}자를 초과하면 의미 단위 경계에서 분리해라.
- **5자 미만 구절은 절대 금지 — 반드시 인접 구절과 합쳐라.**
- **한 문장을 2~3개 구절로 나눠라 (4개 이상 분할 금지)**
- 인접 구절끼리 길이가 균등하도록 분리해라.
- 좋은 예:
  - ["고사리 볶을 때", "뚜껑 열고 뒤적거리기만", "하시나요?"] → 3개 ✅
  - ["비엔나 소세지에 칼집을 내고", "끓는 물에 살짝 데쳐서", "사용해주세요"] → 3개 ✅
  - ["비법이 대체 뭘까요?"] → 짧은 문장은 1개 통째 ✅
- 나쁜 예:
  - ["고사리", "볶을 때"] → 너무 짧음 ❌
  - ["비엔나 소세지에", "칼집을 내고", "끓는 물에", "살짝 데쳐서", "사용해주세요"] → 5개, 너무 잘게 분할 ❌
  - ["뚜껑을 덮어 수분을 가두고 마늘과 대파를", "넣어"] → 불균등 ❌
"""
    + _SUBTITLE_PHRASE_SCENE_GUIDE
    + _SUBTITLE_PHRASE_OUTPUT
)


# ============================================================
# Static 모드 전용 프롬프트
# ============================================================
SUBTITLE_EFFECT_PROMPT_STATIC = (
    _SUBTITLE_COMMON_INTRO
    + _SUBTITLE_COMMON_EFFECTS
    + _SUBTITLE_COMMON_RULES
    + """
==============================================
[static 모드 규칙]
==============================================
- 문장 전체를 한 번에 표시합니다.
- 어절 분리 불필요: words에 문장 전체를 하나의 text로 넣어라.
- display는 반드시 "static"으로 지정해라.
"""
    + _SUBTITLE_COMMON_SCENE_GUIDE
    + _SUBTITLE_COMMON_OUTPUT
)


# ============================================================
# 프롬프트 선택 헬퍼
# ============================================================
SUBTITLE_PROMPT_MAP = {
    "single": SUBTITLE_EFFECT_PROMPT_SINGLE,
    "stack": SUBTITLE_EFFECT_PROMPT_STACK,
    "phrase": SUBTITLE_EFFECT_PROMPT_PHRASE,
    "static": SUBTITLE_EFFECT_PROMPT_STATIC,
    "accumulate": SUBTITLE_EFFECT_PROMPT_SINGLE,  # accumulate는 single과 동일한 분리 규칙
}

def get_subtitle_prompt(subtitle_mode: str) -> str:
    """모드에 맞는 프롬프트 템플릿을 반환합니다."""
    return SUBTITLE_PROMPT_MAP.get(subtitle_mode, SUBTITLE_EFFECT_PROMPT_SINGLE)
