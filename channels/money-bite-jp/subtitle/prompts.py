"""
Money Bite 채널 자막 효과 프롬프트
모드별 전용 프롬프트 (공통 규칙 + 모드별 어절 분리 규칙)
"""

# ============================================================
# 공통 규칙 (모든 모드에서 공유)
# ============================================================
_SUBTITLE_COMMON_INTRO = """너는 유튜브 쇼츠 금융 교육 영상의 **자막 효과 연출 전문가**다.
아래 대본을 분석하여, 각 문장의 어절을 자연스러운 단위로 나누고
어떤 어절에 효과를 줄지 결정해라.
또한 강조할 키워드에 색상을 지정해라.

[대본]
{script_text}
"""

_SUBTITLE_COMMON_EFFECTS = """
==============================================
[사용 가능한 효과 — 2가지만 사용]
==============================================
1. "bounce" — 톡 튀어나오는 크기 변화 (강조할 어절에만)
2. null — 효과 없음 (일반 어절)

==============================================
[사용 가능한 글자 색상 — 3가지]
==============================================
1. "#FFD700" — 금색 (금융 용어, 핵심 개념, 강조)
2. "#FF3333" — 빨간색 (경고, 위험, 하락, 손실)
3. "#00FF88" — 녹색 (팁, 해결책, 상승, 수익)
※ 지정 안 된 어절은 자동으로 흰색
"""

_SUBTITLE_COMMON_RULES = """
==============================================
[공통 규칙 — 반드시 지켜라]
==============================================
1. **대본의 모든 scene을** 출력해라
2. **한 문장에 bounce는 최대 2개**까지만 (나머지는 전부 null)
3. **words의 text를 순서대로 이어붙이면 원래 문장과 단 한 글자도 빠짐없이 정확히 같아야 한다**
   - 원문: "普通株と優先株の違いって何？"
   - words: ["普通株と", "優先株の", "違いって何？"]
   → 합치면: "普通株と優先株の違いって何？" ✅
   - **단어를 수정, 생략, 추가하지 마라. 절대 다른 언어(한국어 등)로 번역하지 마라.**
   - **(가장 중요) 'でも'를 '에서도'로, '的に'를 '적으로' 같이 일본어 조사를 한국어로 절대 변역/변형하지 마라. 무조건 원본 일본어 글자 그대로 유지해라.**
4. **어절 분리는 철저히 '의미 단위(의미상 한 호흡)'로 묶어라 (단순 글자 수 끊기 절대 금지)**
   - (핵심) 명사와 조사, 복합 명사를 절대 중간에 쪼개지 마라.
   - 나쁜 예: ["前に事前", "予約をすると"] → 事前/予約 의미가 끊어짐 ❌
   - 좋은 예: ["前に", "事前予約をすると"] 또는 ["事前予約を", "すると"] ✅
   - 나쁜 예: ["大作", "ゲームが"] → 너무 짧음 (글자 수 압박) ❌
   - 좋은 예: ["大作ゲームが"] ✅
5. **color_keywords에는 색상을 줄 키워드만** 포함 (대부분 어절은 지정 불필요)
"""

_SUBTITLE_COMMON_SCENE_GUIDE = """
==============================================
[Scene별 가이드라인]
==============================================
- Scene 1 (Hook 직관적 질문형): display=single
  → 금융 용어에 bounce + 금색

- Scene 2~6 (본문 설명): display=single
  → 핵심 금융 용어에 bounce + 금색
  → 경고/위험 관련 어절에 빨간색
  → 팁/해결책 어절에 녹색
  → 효과는 장면당 0~2개로 절제

- Scene 7 (한 줄 요약): display=single
  → 핵심 교훈 어절에 bounce + 금색 또는 녹색
"""

_SUBTITLE_COMMON_OUTPUT = """
==============================================
[출력 형식]
==============================================
{{
    "scenes": [
        {{
            "scene_id": 1,
            "display": "single",
            "words": [
                {{"text": "PERって", "effect": "bounce"}},
                {{"text": "何？", "effect": null}}
            ]
        }},
        ...
    ],
    "color_keywords": {{
        "#FFD700": ["PERって"],
        "#FF3333": [],
        "#00FF88": []
    }}
}}

⚠️ 위 형식 그대로 JSON만 출력해라. 설명, 부연, 마크다운, 코드블록 금지.
⚠️ 반드시 모든 scene을 포함하고 color_keywords도 포함해라.
"""

# Phrase 모드 전용 Scene 가이드 (display=phrase)
_SUBTITLE_PHRASE_SCENE_GUIDE = """
==============================================
[Scene별 가이드라인 — phrase 모드]
==============================================
- Scene 1 (Hook 직관적 질문형): display=phrase
  → 금융 용어에 bounce + 금색
  → 문장을 2~3개 의미 구절로 나눠라

- Scene 2~6 (본문 설명): display=phrase
  → 핵심 금융 용어에 bounce + 금색
  → 경고/위험 관련 어절에 빨간색
  → 팁/해결책 어절에 녹색
  → 효과는 장면당 0~2개로 절제
  → 문장을 2~3개 의미 구절로 나눠라

- Scene 7 (한 줄 요약): display=phrase
  → 핵심 교훈 어절에 bounce + 금색 또는 녹색
  → 문장을 2~3개 의미 구절로 나눠라

※ 좋아요 CTA 등 short 문장이 있으면 display=static으로 처리
"""

# Phrase 모드 전용 출력 형식
_SUBTITLE_PHRASE_OUTPUT = """
==============================================
[출력 형식 — phrase 모드]
==============================================
입력 대본:
1. EPSが何かっていうと会社が稼いだ純利益を株式数で割ったものだよ

출력 예시:
{{
    "scenes": [
        {{
            "scene_id": 1,
            "display": "phrase",
            "words": [
                {{"text": "EPSが何かっていうと", "effect": "bounce"}},
                {{"text": "会社が稼いだ純利益を", "effect": null}},
                {{"text": "株式数で割ったものだよ", "effect": null}}
            ]
        }},
        ...
    ],
    "color_keywords": {{
        "#FFD700": ["EPSが何かっていうと"],
        "#FF3333": [],
        "#00FF88": []
    }}
}}

⚠️ 위 형식 그대로 JSON만 출력해라. 설명, 부연, 마크다운, 코드블록 금지.
⚠️ 반드시 모든 scene을 포함하고 color_keywords도 포함해라.
⚠️ 각 구절은 반드시 5자 이상이어야 한다. 짧은 구절은 인접 구절과 합쳐라.
"""


# ============================================================
# Single 모드 전용 프롬프트
# ============================================================
SUBTITLE_EFFECT_PROMPT_SINGLE = (
    _SUBTITLE_COMMON_INTRO
    + _SUBTITLE_COMMON_EFFECTS
    + _SUBTITLE_COMMON_RULES
    + """
==============================================
[single 모드 어절 분리 규칙]
==============================================
- 한 어절이 화면 중앙에 **단독으로** 표시됩니다.
- 한 묶음은 최소 **2자**, 최대 **{max_chunk_chars}자**(공백 포함).
- {max_chunk_chars}자를 초과하면 의미 단위 경계에서 분리해라.
"""
    + _SUBTITLE_COMMON_SCENE_GUIDE
    + _SUBTITLE_COMMON_OUTPUT
)


# ============================================================
# Stack 모드 전용 프롬프트
# ============================================================
SUBTITLE_EFFECT_PROMPT_STACK = (
    _SUBTITLE_COMMON_INTRO
    + _SUBTITLE_COMMON_EFFECTS
    + _SUBTITLE_COMMON_RULES
    + """
==============================================
[stack 모드 어절 분리 규칙]
==============================================
- 어절이 2줄로 쌓이며 표시됩니다 (1줄씩 추가, 최대 2줄).
- 한 묶음은 최소 **4자**, 최대 **{max_chunk_chars}자**(공백 포함).
- **3자 이하 어절은 반드시 인접 어절과 합쳐라.** (한 줄이 너무 허전하면 안 됨)
  - 나쁜 예: ["発行された"] (5자) → 한 줄을 혼자 차지하면 허전 ❌
  - 좋은 예: ["発行された総株式数で"] ✅
  - 나쁜 예: ["割った"] (3자) ❌
  - 좋은 예: ["割った一株当たり純利益だよ"] ✅
- 인접한 두 어절의 길이 차이가 크지 않도록 **균등하게** 분리해라.
"""
    + _SUBTITLE_COMMON_SCENE_GUIDE
    + _SUBTITLE_COMMON_OUTPUT
)


# ============================================================
# Phrase 모드 전용 프롬프트
# ============================================================
SUBTITLE_EFFECT_PROMPT_PHRASE = (
    _SUBTITLE_COMMON_INTRO
    + _SUBTITLE_COMMON_EFFECTS
    + _SUBTITLE_COMMON_RULES
    + """
==============================================
[phrase 모드 어절 분리 규칙]
==============================================
- 구절(phrase)이 화면 중앙에 표시되고, 새 구절이 나오면 **이전 구절을 완전히 교체**합니다.
- 한 구절 = **의미상 완결된 한 호흡** (주어+서술어, 명사+조사+동사 등)
- 한 구절은 최소 **5자**, 최대 **{max_chunk_chars}자**(공백 포함).
- {max_chunk_chars}자를 초과하면 의미 단위 경계에서 분리해라.
- **5자 미만 구절은 절대 금지 — 반드시 인접 구절과 합쳐라.**
- **한 문장을 2~3개 구절로 나눠라 (4개 이상 분할 금지)**
- 인접 구절끼리 길이가 균등하도록 분리해라.
- 좋은 예:
  - ["EPSが何かっていうと", "会社が稼いだ純利益を", "株式数で割ったものだよ"] → 3개 ✅
  - ["貯金より利回りが高いから", "長期的に投資すれば", "複利効果を得られるよ"] → 3개 ✅
  - ["PERって何？"] → 짧은 문장은 1개 통째 ✅
- 나쁜 예:
  - ["EPSが", "何？"] → 너무 짧음 ❌
  - ["会社が", "1年間", "稼いだ", "純利益を", "割ったものだよ"] → 5개, 너무 잘게 분할 ❌
  - ["会社が1年間稼いだ純利益を株式数で", "割った"] → 불균등 ❌
"""
    + _SUBTITLE_PHRASE_SCENE_GUIDE
    + _SUBTITLE_PHRASE_OUTPUT
)


# ============================================================
# Static 모드 전용 프롬프트
# ============================================================
SUBTITLE_EFFECT_PROMPT_STATIC = (
    _SUBTITLE_COMMON_INTRO
    + _SUBTITLE_COMMON_EFFECTS
    + _SUBTITLE_COMMON_RULES
    + """
==============================================
[static 모드 규칙]
==============================================
- 문장 전체를 한 번에 표시합니다.
- 어절 분리 불필요: words에 문장 전체를 하나의 text로 넣어라.
- display는 반드시 "static"으로 지정해라.
"""
    + _SUBTITLE_COMMON_SCENE_GUIDE
    + _SUBTITLE_COMMON_OUTPUT
)


# ============================================================
# 프롬프트 선택 헬퍼
# ============================================================
SUBTITLE_PROMPT_MAP = {
    "single": SUBTITLE_EFFECT_PROMPT_SINGLE,
    "stack": SUBTITLE_EFFECT_PROMPT_STACK,
    "phrase": SUBTITLE_EFFECT_PROMPT_PHRASE,
    "static": SUBTITLE_EFFECT_PROMPT_STATIC,
    "accumulate": SUBTITLE_EFFECT_PROMPT_SINGLE,  # accumulate는 single과 동일한 분리 규칙
}

def get_subtitle_prompt(subtitle_mode: str) -> str:
    """모드에 맞는 프롬프트 템플릿을 반환합니다."""
    return SUBTITLE_PROMPT_MAP.get(subtitle_mode, SUBTITLE_EFFECT_PROMPT_SINGLE)
